// operador switch --de acuerdo a la opcion

let opcionMenu = 2

switch (opcionMenu) {
  case 1:
    console.log("seleccionó Pizza")
    break
  case 2:
    console.log("seleccionó arroz con pollo")
    break
  case 3:
    console.log("seleccionó Hotdog")
    break
  case 4:
    console.log("seleccionó frijoles con garra")
    break
  default:
    console.log("bobolio, escoja una opcion válida !")
    break
}
